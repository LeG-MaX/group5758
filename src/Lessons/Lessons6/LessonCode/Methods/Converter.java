package Lessons.Lessons6.LessonCode.Methods;

public class Converter {

    public double converterCelsiumToKelvin(double temperaturaInCelsium){
        double temperaturaInKelvin = temperaturaInCelsium - 273.15;
        return temperaturaInKelvin;
    }

    public double temperaturaCelsiaInFaringate(double temperaturaInCelsium){
        double temperaturaInFaringate =temperaturaInCelsium*1.8+32;
        return temperaturaInFaringate;
    }

    //public void
}
